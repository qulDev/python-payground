# Demonstrasi tipe data di Python

# Tipe data Number
integer_number = 10
float_number = 3.14
complex_number = 1 + 2j

# Tipe data String
text = "Halo Python!"
multiline_text = '''
Ini adalah contoh
teks yang memiliki
beberapa baris
'''

# Tipe data Boolean
is_true = True
is_false = False

# Tipe data List
fruits = ["apel", "jeruk", "mangga"]
mixed_list = [1, "dua", 3.0, True]

# Tipe data Tuple
coordinates = (10, 20)
rgb_color = (255, 128, 0)

# Tipe data Dictionary
person = {
    "name": "John Doe",
    "age": 30,
    "is_student": False
}

# Tipe data Set
unique_numbers = {1, 2, 3, 3, 4, 4, 5}  # Duplikat akan dihapus

# Demonstrasi penggunaan
print("=== Demonstrasi Tipe Data ===")
print(f"Integer: {integer_number}")
print(f"Float: {float_number}")
print(f"Complex: {complex_number}")
print(f"String: {text}")
print(f"List: {fruits}")
print(f"Tuple: {coordinates}")
print(f"Dictionary: {person}")
print(f"Set: {unique_numbers}")
